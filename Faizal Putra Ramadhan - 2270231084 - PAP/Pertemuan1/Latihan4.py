#tipe data

#tipe data Boolean
print(True)

#tipe data String
print("Ayo Belajar Phyton")
print('Belajar Phyton Sangat Mudah')

#tipe data Integer
print(20)

#tipe data Hexadecimal
print("9a")

#tipe data Complex
print(5j)

#tipe data List
print([1,2,3,4,5])
print(["satu", "dua", "tiga"])

#tipe data Dictionary
print({"nama":"Budi", 'umur':20})
#tipe data Dictionary dimasukkan ke dalam variabel biodata
biodata = {"nama":"Andi", 'umur':21} #proses irasionalisai variabel biodata
print(biodata) #proses pencetakan variabel biodata yang berisi tipe 
# data dictionary
print(type(biodata)) # Fungsi untuk mengecek jenis tipe data. Akan tampil <class 'dict'> yang berarti dict adalah tipe data dictionary